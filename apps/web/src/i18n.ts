export type TranslationKey = keyof typeof fa

export function useI18n() {
  const ui = useUiStore()
  const language = computed(() => ui.preferences.uiLanguage)
  const isRtl = computed(() => language.value === 'fa')
  const t = (key: TranslationKey): string => (language.value === 'fa' ? fa[key] : en[key])
  const localize = (value: { fa?: string | undefined; en?: string | undefined } | undefined, fallback = ''): string => {
    if (!value) return fallback
    return language.value === 'fa' ? value.fa ?? value.en ?? fallback : value.en ?? value.fa ?? fallback
  }
  return { language, isRtl, t, localize }
}
